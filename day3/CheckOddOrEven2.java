package week1.day3;

public class CheckOddOrEven2 {

	// for Loop Syntax
	// for (initialization ;condition;increment/decrement ){ }

	public static void main(String[] args) {

		System.out.println("The odd Numbers are:");
		for (int i = 1; i <= 10; i++) {

			if (i % 2 != 0) {
				System.out.println(i);
			}

		}

	}

}
