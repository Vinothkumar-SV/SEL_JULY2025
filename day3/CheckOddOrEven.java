package week1.day3;

public class CheckOddOrEven {

	public static void main(String[] args) {

		// 1/2= 0.5 %
		int number = 26;
		if (number % 2 == 0) {
			System.out.println("Number " + number + " is" + " Even");
		}

		else {
			System.out.println("Number " + number + " is " + " Odd");
		}

	}

}
